// Fun√ß√£o para processar notifica√ß√µes pendentes (chamar na Home)
window.processarNotificacoesPendentes = async function() {
    console.log('üîÑ Processando notifica√ß√µes pendentes...');
    try {
        const notificacoesPendentesString = localStorage.getItem('notificacoesPendentes');
        console.log('üì± LocalStorage notificacoesPendentes:', notificacoesPendentesString);
        
        const notificacoesPendentes = JSON.parse(notificacoesPendentesString || '[]');
        console.log('üìã Notifica√ß√µes pendentes encontradas:', notificacoesPendentes.length);
        
        if (notificacoesPendentes.length > 0) {
            console.log('üìå Notifica√ß√µes Manager dispon√≠vel:', !!window.notificacoesManager);
            
            if (window.notificacoesManager) {
                for (const notificacao of notificacoesPendentes) {
                    console.log('‚è∞ Processando notifica√ß√£o:', notificacao);
                    
                    // Criar notifica√ß√£o se foi criada nos √∫ltimos 5 minutos
                    if (Date.now() - notificacao.timestamp < 5 * 60 * 1000) {
                        const { timestamp, ...dadosNotificacao } = notificacao;
                        console.log('‚úÖ Criando notifica√ß√£o:', dadosNotificacao);
                        await window.notificacoesManager.criarNotificacao(dadosNotificacao);
                    } else {
                        console.log('‚è≥ Notifica√ß√£o muito antiga, ignorando:', notificacao);
                    }
                }
                
                // Limpar notifica√ß√µes pendentes
                localStorage.removeItem('notificacoesPendentes');
                console.log('üóëÔ∏è Notifica√ß√µes pendentes removidas do localStorage');
            } else {
                console.log('‚ùå Notifica√ß√µes Manager n√£o dispon√≠vel');
            }
        } else {
            console.log('üì≠ Nenhuma notifica√ß√£o pendente encontrada');
        }
    } catch (error) {
        console.error('‚ùå Erro ao processar notifica√ß√µes pendentes:', error);
    }
};